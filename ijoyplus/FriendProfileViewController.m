- (void)viewDidLoad
{
    [super viewDidLoad];
    self.title = NSLocalizedString(@"好友名称", nil);
    UIBarButtonItem *leftButton = [[UIBarButtonItem alloc] initWithTitle:NSLocalizedString(@"go_back", nil) style:UIBarButtonSystemItemSearch target:self action:@selector(closeSelf)];
    self.navigationItem.leftBarButtonItem = leftButton;
    
    rightButton = [[UIBarButtonItem alloc] initWithTitle:NSLocalizedString(@"follow", nil) style:UIBarButtonSystemItemSearch target:self action:@selector(follow)];
    self.navigationItem.rightBarButtonItem = rightButton;
}

- (void)closeSelf
{
    [self.navigationController popViewControllerAnimated:YES];
}

- (void)follow
{
    [rightButton setTitle:NSLocalizedString(@"cancel_follow",nil)];
    [rightButton setAction:@selector(cancelFollow)];
}

- (void)cancelFollow
{
    [rightButton setTitle:NSLocalizedString(@"follow", nil)];
    [rightButton setAction:@selector(follow)];
}

